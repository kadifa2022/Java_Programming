package day06_selfPractice;

public class WeekDays {
    public static void main(String[] args) {

        int n = 5;
        String weekDays = "";

        if (n == 1) {
            weekDays = " Monday";
        }
        if (n == 2) {
            weekDays = " Tuesday";
        }
        if (n == 3) {
            weekDays = " Wednesday ";
        }
        if (n == 4) {
            weekDays = " Thursday ";
        }
        if (n == 5) {
            weekDays = " Friday ";
        }
        if (n == 6) {
            weekDays = " Saturday ";
        }
        if (n == 7) {
            weekDays = " Sunday ";
        }
        System.out.println(weekDays);


    }
}
