package day06_selfPractice;

public class MonthName {
    public static void main(String[] args) {

        int n = 1;

        String monthName = "";

        if (n == 1) {
            monthName = " January ";
        }
        if (n == 2) {
            monthName = " February ";
        }
        if (n == 3) {
            monthName = " March ";
        }
        if (n == 4) {
            monthName = " April ";
        }
        if (n == 5) {
            monthName = " May ";
        }
        if (n == 6) {
            monthName = "June";
        }
        if (n == 7) {
            monthName = " July ";
        }
        if (n == 8) {
            monthName = " August ";
        }
        if (n == 9) {
            monthName = " September ";
        }
        if (n == 10) {
            monthName = " October";
        }
        if (n == 11) {
            monthName = " November ";
        }
        if (n == 12) {
            monthName = " December ";
        }

        System.out.println(monthName);


    }
}
